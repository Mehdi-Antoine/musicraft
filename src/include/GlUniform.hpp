#pragma once

#include <GL/glew.h>

#include <glimac/glm.hpp>



//--------------------------------------------------------------------------------------------------------------
//----------------------------------------------CLASS GL_UNIFORM------------------------------------------------
//-------------------------------------------------(VIRTUAL)----------------------------------------------------
//--------------------------------------------------------------------------------------------------------------

class GlUniform{
private:
	
	
public:

//-------------------------------------------------CONSTRUCTOR--------------------------------------------------

	GlUniform();
	virtual ~GlUniform() = 0;

//--------------------------------------------------FUNCTIONS---------------------------------------------------

};


//--------------------------------------------------------------------------------------------------------------
//--------------------------------------------CLASS GL_UNIFORM_VEC3---------------------------------------------
//--------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------

class GlUniformVec3 : public GlUniform{
private:

	GLint     m_id;	
	glm::vec3 m_data;
	
public:

//-------------------------------------------------CONSTRUCTOR--------------------------------------------------

	GlUniformVec3(GLuint program_id, const char* uniform_name);
	GlUniformVec3();
	~GlUniformVec3();

//--------------------------------------------------FUNCTIONS---------------------------------------------------

	void init(GLuint program_id, const char* uniform_name);

	void update(glm::vec3 new_data);

};

