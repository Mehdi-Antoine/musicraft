#include <vector>


enum vboType{ 
	POSITION, 
	COLOR
};


class GlElement{
private:

	int m_size;

	ShaderProgram m_shaders;

	Glenum m_draw_mode;

	GLuint m_vao;

	GLuint m_color_vbo;
	GLuint m_position_vbo;

public:

//--------------------------------------------------------------------------------------------------------------
//------------------------------------------------CONSTRUCTOR---------------------------------------------------
//--------------------------------------------------------------------------------------------------------------

	GlElement(std::vector<glm::vec3> &position_index, 
			  std::vector<glm::vec3> &color_index, 
			  char*                  dirPath, 
			  char*                  shader_type);

	~GlElement();

//--------------------------------------------------------------------------------------------------------------
//--------------------------------------------------SETTERS-----------------------------------------------------
//--------------------------------------------------------------------------------------------------------------

	void setDrawMode(GLenum drawmode);

//--------------------------------------------------------------------------------------------------------------
//-------------------------------------------------FUNCTIONS----------------------------------------------------
//--------------------------------------------------------------------------------------------------------------

	void bind(vboType type);

	void unbind();

	void data(std::vector<glm::vec3> position_index, std::vector<glm::vec3> color_index);

	void draw();

};